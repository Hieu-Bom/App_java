public void laydlb() throws SQLException{
        try {
        ketnoi kn = null;
        kn = new ketnoi();
        ResultSet rs = null; 
        rs = kn.LayDL2("gvtg");
        DefaultTableModel m1 = (DefaultTableModel)tbgvtg.getModel();
        m1.setRowCount(20);
        int i = 0;
            while (rs.next()) {
                if (rs.getString(1)!=null){
                    tbgvtg.setValueAt(rs.getString(1), i, 0);
                    tbgvtg.setValueAt(rs.getString(2), i, 1);
                    tbgvtg.setValueAt(rs.getString(3), i, 2);
                    tbgvtg.setValueAt(rs.getString(4), i, 3);
                    tbgvtg.setValueAt(rs.getString(5), i, 4);
                    tbgvtg.setValueAt(rs.getString(6), i, 5);
                    tbgvtg.setValueAt(rs.getString(7), i, 6);
                }
                i++;
            }
        } catch (SQLException ex) {
             System.out.println("Lỗi lấy bảng dữ liệu!");
             JOptionPane.showMessageDialog(null,"Lỗi lấy bảng dữ liệu","Lỗi", JOptionPane.WARNING_MESSAGE);
        }
}

    private void btntimkiemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btntimkiemMouseClicked
        // TODO add your handling code here:
        try {
            ketnoi ketNoi = new ketnoi();
            DefaultTableModel m1 = (DefaultTableModel)tbgvtg.getModel();
            m1.setRowCount(1);
            ResultSet rs = ketNoi.TimkiemTG(txttk.getText());
            while (rs.next()) {
                tbgvtg.setValueAt(rs.getString(1), 0, 0);
                tbgvtg.setValueAt(rs.getString(2), 0, 1);
                tbgvtg.setValueAt(rs.getString(3), 0, 2);
                tbgvtg.setValueAt(rs.getString(4), 0, 3);
                tbgvtg.setValueAt(rs.getString(5), 0, 4);
                tbgvtg.setValueAt(rs.getString(6), 0, 5);
                tbgvtg.setValueAt(rs.getString(7), 0, 6);
            }
        } catch (Exception e) {
            System.out.println("Lỗi xóa!");
            JOptionPane.showMessageDialog(null,"Lỗi Tim Kiem","Lỗi", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btntimkiemMouseClicked

